// Undefined
// Kintamasis, kuriam nebuvo priskirta jokia reiksme.
let akiuSpalva;
// Null
// Reiksme, kuri reiskia, kad kintamasis yra tuscias (dar nezinoma reiksme)
let automobilioMarke = null;

